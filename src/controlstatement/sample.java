package controlstatement;

public class sample {

	public static void main(String[] args) {
		int a = 20;
		
		System.out.println(a);
		
		//post increment
		System.out.println(a++);
		System.out.println(a);
		
		//post decrement
		System.out.println(a--);
		System.out.println(a);
		
		//per increment
		System.out.println(a);
		System.out.println(++a);
		System.out.println(++a);
		System.out.println(a);
		
		//per decrement
		System.out.println(--a);
		System.out.println(--a);
		System.out.println(a);
		
		System.out.println(+-a);
		System.out.println(a);
		
	}
}
